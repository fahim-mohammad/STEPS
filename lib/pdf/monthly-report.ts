import fs from 'fs'
import path from 'path'

export type MonthlyFundReportInput = {
  language: 'en' | 'bn'
  month: number
  year: number
  fundBalance: number
  totalInvested: number
  totalCharity: number
  approvedMembers: number
  collectionsTrend: Array<{ monthLabel?: string; year?: number; approvedTotal?: number; pendingTotal?: number }>
  chairmanSignatureUrl?: string | null
  accountantSignatureUrl?: string | null
}

function bnDigits(input: string) {
  const map: Record<string, string> = {
    '0': '০', '1': '১', '2': '২', '3': '৩', '4': '৪',
    '5': '৫', '6': '৬', '7': '৭', '8': '৮', '9': '৯',
  }
  return input.replace(/[0-9]/g, (d) => map[d] || d)
}

function money(n: number, lang: 'en' | 'bn') {
  const s = `৳${Number(n || 0).toLocaleString()}`
  return lang === 'bn' ? bnDigits(s) : s
}

