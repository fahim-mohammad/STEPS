export function useTranslations(language: Language) {
  const t = (key: keyof typeof translations.en | string, params?: Params) => {
    const dict: any = translations[language] || translations.en
    const base: any = translations.en

    const raw =
      dict?.[key as any] ??
      base?.[key as any] ??
      (typeof key === 'string'
        ? language === 'bn'
          ? autoBnFromEnglish(humanizeKey(key))
          : humanizeKey(key)
        : String(key))

    return applyParams(String(raw), params)
  }

  return { t }
}