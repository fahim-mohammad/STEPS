export function formatMonthLabel(month: number, year: number, language: Language = 'en') {
  const safeMonth = Math.min(12, Math.max(1, Number(month) || 1))
  const safeYear = Number(year) || new Date().getFullYear()
  const d = new Date(safeYear, safeMonth - 1, 1)
  const locale = language === 'bn' ? 'bn-BD' : 'en-US'
  const m = d.toLocaleString(locale, { month: 'long' })
  return `${m} ${safeYear}`
}

